// No. 2
// Tulislah sebuah function dengan nama kalikan() yang mengembalikan hasil perkalian dua parameter yang di kirim melalui
// process.argv

function kalikan(num1, num2) {
  return num1 * num2;
}

// TEST CASE
console.log(kalikan(4, 12)); // 48

// SAMPLE TEST
console.log(kalikan(100, 3)); // 300
console.log(kalikan(2, 3)); // 6
console.log(kalikan(15634, 1213)); // 18964042
